    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // TODO add your handling code here:
        String kode = txt_kode.getText();
        int daya = Integer.parseInt(txt_daya.getText());
        int tarif = Integer.parseInt(txt_tarif.getText());

        try {
            String sql = "insert into tarif (kode_tarif, daya, tarif_per_kwh) "
                    + "values(?,?,?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, kode);
            pst.setInt(2, daya);
            pst.setInt(3, tarif);
            pst.execute();
            JOptionPane.showMessageDialog(this, "Tambah Data tarif berhasil");
            getDataTarif();
            btn_baruActionPerformed(evt);
            txt_kode.setEditable(true);
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Tambah Data tarif Gagal");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

