    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:
        String noMeter = txt_keyword.getText();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            String sql = "select * from tarif where no_meter LIKE '%" + noMeter + "%'";
            stat = conn.createStatement();
            res = stat.executeQuery(sql);
            while (res.next()) {
                Object[] o = new Object[6];
                o[0] = res.getString("no_meter");
                o[1] = res.getString("daya");
                o[2] = res.getString("jumlah_kwh");
                o[3] = res.getString("nama");
                o[4] = res.getString("alamat");
                o[5] = res.getString("golongan");
                model.addRow(o);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_cariActionPerformed

