    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:
        String noMeter = txt_keyword.getText();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            String sql = "select * from pelanggan where no_meter LIKE '%" + noMeter + "%'";
            stat = conn.createStatement();
            res = stat.executeQuery(sql);
            while (res.next()) {
                Object[] o = new Object[7];
                o[0] = res.getString("no_meter");
                o[1] = res.getString("daya");
                o[2] = res.getString("jumlah_kwh");
                o[3] = res.getString("nama");
                o[4] = res.getString("alamat");
                o[5] = res.getString("total_tagihan");
                o[6] = res.getString("tanggal_join");
                model.addRow(o);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_cariActionPerformed

    private void txt_kwhKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_kwhKeyReleased
        // TODO add your handling code here:
        try {
            String sql = "select * from tarif where daya='" + cb_daya.getSelectedItem().toString() + "'";
            stat = conn.createStatement();
            res = stat.executeQuery(sql);
            if (res.next()) {
                int tarif = Integer.parseInt(res.getString("tarif_per_kwh"));
                int jumlah = Integer.parseInt(txt_kwh.getText());
                int total = tarif * jumlah;
                lbl_total_tagihan.setText(String.valueOf(total));
            }
        } catch (NumberFormatException | SQLException e) {
        }
    }//GEN-LAST:event_txt_kwhKeyReleased

