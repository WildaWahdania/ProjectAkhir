    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // TODO add your handling code here:
        String noMeter = txt_noMeter.getText();
        int daya = Integer.parseInt(cb_daya.getSelectedItem().toString());
        int jumlahKWH = Integer.parseInt(txt_kwh.getText());
        String nama = txt_nama.getText();
        String alamat = txt_alamat.getText();
        int total = Integer.parseInt(lbl_total_tagihan.getText());
        
        String tanggal = String.valueOf(now);

        try {
            String sql = "insert into pelanggan (no_meter, daya, jumlah_kwh, nama, alamat, total_tagihan, tanggal_join, no_telp) "
                    + "values(?,?,?,?,?,?,?, ?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, noMeter);
            pst.setInt(2, daya);
            pst.setInt(3, jumlahKWH);
            pst.setString(4, nama);
            pst.setString(5, alamat);
            pst.setInt(6, total);
            pst.setString(7, tanggal);
            pst.setInt(8, Integer.parseInt(txt_notelp.getText()));
            pst.execute();
            JOptionPane.showMessageDialog(this, "Tambah Data pelanggan berhasil");
            getDataPelanggan();
            btn_baruActionPerformed(evt);
            txt_noMeter.setEditable(true);
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Tambah Data pelanggan Gagal");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

